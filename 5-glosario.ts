// Glosario de conceptos TypeScript aprendidos

// 1. Tipos básicos: Representan valores primitivos como string, number, boolean.
//    Ejemplo: let age: number = 25;

// 2. Unión: Permite que una variable tenga más de un tipo.
//    Ejemplo: let id: string | number = "ABC";

// 3. Interfaces: Definen la estructura de un objeto.
//    Ejemplo: interface Person { name: string; age: number; }

// 4. Funciones con tipado: Especifican tipos para parámetros y retorno.
//    Ejemplo: function sum(a: number, b: number): number { return a + b; }

// 5. Tipos literales: Restringen un tipo a valores específicos.
//    Ejemplo: type Status = "active" | "inactive";

// 6. Enums: Conjunto de constantes nombradas.
//    Ejemplo: enum Color { Red, Green, Blue }

// 7. Tipado fuerte: TypeScript requiere tipos explícitos para mayor seguridad.
//    Ejemplo: let name: string = "Juan";

// 8. Compilación: Convierte código TypeScript a JavaScript.
//    Ejemplo: tsc archivo.ts genera archivo.js